/**
 * Replace {{placeholders}} in a string with values from context.
 * @param {string} str - String with placeholders
 * @param {Object} vars - Variables to replace
 * @returns {string} - String with placeholders replaced
 */
function replacePlaceholders(str, vars) {
  if (!str || typeof str !== 'string') return str;
  let result = str;
  
  // Replace all placeholders with safety checks
  for (const [key, value] of Object.entries(vars)) {
    if (value !== undefined && value !== null) {
      const regex = new RegExp(`{{${key}}}`, 'g');
      const safeValue = String(value).replace(/[\\<>]/g, ''); // Sanitize HTML-like characters
      result = result.replace(regex, safeValue);
    }
  }
  
  // Remove any remaining placeholders
  result = result.replace(/{{[^}]+}}/g, '[unknown]');
  
  return result;
}

/**
 * Parse color value from various formats.
 * @param {string|number|Array} color - Color in various formats
 * @returns {number|null} - Parsed color integer
 */
function parseColor(color) {
  if (color === undefined || color === null) return null;
  
  // Number (hex)
  if (typeof color === 'number') {
    if (color >= 0 && color <= 0xFFFFFF) return color;
    return null;
  }
  
  // String
  if (typeof color === 'string') {
    // Hex string
    if (color.startsWith('#')) {
      const parsed = parseInt(color.slice(1), 16);
      return isNaN(parsed) ? null : parsed;
    }
    // Named color
    const lowerColor = color.toLowerCase();
    if (namedColors[lowerColor]) {
      return namedColors[lowerColor];
    }
  }
  
  // RGB array
  if (Array.isArray(color) && color.length === 3) {
    const [r, g, b] = color;
    if (r >= 0 && r <= 255 && g >= 0 && g <= 255 && b >= 0 && b <= 255) {
      return (r << 16) + (g << 8) + b;
    }
  }
  
  return null;
}

/**
 * Truncate string to Discord embed limits
 * @param {string} str - String to truncate
 * @param {number} maxLength - Maximum length
 * @returns {string} - Truncated string
 */
function truncateString(str, maxLength = 1024) {
  if (!str || str.length <= maxLength) return str;
  return str.substring(0, maxLength - 3) + '...';
}

/**
 * Build pagination row for multi-page embeds.
 * @param {number} currentPage - Current page index (0-based)
 * @param {number} totalPages - Total number of pages
 * @param {string} customIdPrefix - Prefix for button custom IDs
 * @returns {ActionRowBuilder} - Button row
 */
function buildPaginationRow(currentPage, totalPages, customIdPrefix = 'page') {
  const { ActionRowBuilder, ButtonBuilder, ButtonStyle } = require('discord.js');
  
  if (totalPages <= 1) return null;
  
  return new ActionRowBuilder().addComponents(
    new ButtonBuilder()
      .setCustomId(`${customIdPrefix}_prev`)
      .setLabel('◀ Previous')
      .setStyle(ButtonStyle.Secondary)
      .setDisabled(currentPage <= 0),
    new ButtonBuilder()
      .setCustomId(`${customIdPrefix}_info`)
      .setLabel(`${currentPage + 1} / ${totalPages}`)
      .setStyle(ButtonStyle.Secondary)
      .setDisabled(true),
    new ButtonBuilder()
      .setCustomId(`${customIdPrefix}_next`)
      .setLabel('Next ▶')
      .setStyle(ButtonStyle.Secondary)
      .setDisabled(currentPage >= totalPages - 1),
  );
}

/**
 * Validate embed size limits
 * @param {EmbedBuilder} embed - The embed to validate
 * @returns {boolean} - Whether the embed is within limits
 */
function validateEmbedSize(embed) {
  const MAX_EMBED_TOTAL = 6000; // Total character limit for all fields + description
  
  let totalLength = 0;
  if (embed.data.description) totalLength += embed.data.description.length;
  if (embed.data.title) totalLength += embed.data.title.length;
  if (embed.data.footer?.text) totalLength += embed.data.footer.text.length;
  if (embed.data.author?.name) totalLength += embed.data.author.name.length;
  
  if (embed.data.fields) {
    for (const field of embed.data.fields) {
      totalLength += (field.name?.length || 0) + (field.value?.length || 0);
    }
  }
  
  if (totalLength > MAX_EMBED_TOTAL) {
    logger.warn(`Embed total length (${totalLength}) exceeds Discord limit (${MAX_EMBED_TOTAL})`);
    return false;
  }
  
  return true;
}

