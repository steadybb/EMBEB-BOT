/**
 * Build a simple success embed.
 * @param {string} message - Success message
 * @param {Object} context - Context for replacements
 * @returns {EmbedBuilder}
 */
function successEmbed(message, context = {}) {
  if (typeof message === 'object' && message !== null) {
    return buildEmbed(templates.success(message.message || 'Success'), { ...context, ...message });
  }
  return buildEmbed(templates.success(message), context);
}

/**
 * Build a simple error embed.
 * @param {string|Error} message - Error message or Error object
 * @param {Object} context - Context for replacements
 * @returns {EmbedBuilder}
 */
function errorEmbed(message, context = {}) {
  let errorMessage = '';
  
  if (message instanceof Error) {
    errorMessage = message.message;
    logger.error(`Error embed created for: ${errorMessage}`);
  } else if (typeof message === 'object' && message !== null) {
    errorMessage = message.message || 'Something went wrong';
    return buildEmbed(templates.error(errorMessage), { ...context, ...message });
  } else {
    errorMessage = message || 'Something went wrong';
  }
  
  return buildEmbed(templates.error(errorMessage), context);
}

/**
 * Build an auto post embed.
 * @param {Object} data - Auto post data
 * @param {Object} context - Context for replacements
 * @returns {EmbedBuilder}
 */
function autoPostEmbed(data, context = {}) {
  if (!data || !data.content) {
    logger.error('Invalid data for autoPostEmbed');
    return errorEmbed('Failed to generate auto post content');
  }
  return buildEmbed(templates.autoPost(data), context);
}

/**
 * Build a quote embed.
 * @param {Object} quoteData - Quote data
 * @param {Object} context - Context for replacements
 * @returns {EmbedBuilder}
 */
function quoteEmbed(quoteData, context = {}) {
  if (!quoteData || !quoteData.total) {
    logger.error('Invalid quote data');
    return errorEmbed('Quote data is incomplete');
  }
  return buildEmbed(templates.quote(quoteData), context);
}

/**
 * Build a stats embed.
 * @param {Object} statsData - Statistics data
 * @param {Object} context - Context for replacements
 * @returns {EmbedBuilder}
 */
function statsEmbed(statsData, context = {}) {
  return buildEmbed(templates.stats(statsData), context);
}

/**
 * Build a maintenance embed.
 * @param {number} duration - Duration in minutes
 * @param {string} details - Maintenance details
 * @returns {EmbedBuilder}
 */
function maintenanceEmbed(duration = 15, details = 'System upgrades') {
  return buildEmbed(templates.maintenance(duration, details));
}

